package cn.worldwalker.game.wyqp.wechat.domain;

import java.util.Date;

public class ProxyModel {
	
	private Integer proxyId;
	private Integer playerId;
	private Integer nickName;
	private Integer mobilePhone;
	private Integer password;
	private Integer wechatNum;
	private Integer realName;
	private Integer idCardNo;
	private Integer extractAmount;
	private Integer remainderAmount;
	private Date createTime;
	
	public Integer getProxyId() {
		return proxyId;
	}
	public void setProxyId(Integer proxyId) {
		this.proxyId = proxyId;
	}
	public Integer getPlayerId() {
		return playerId;
	}
	public void setPlayerId(Integer playerId) {
		this.playerId = playerId;
	}
	public Integer getNickName() {
		return nickName;
	}
	public void setNickName(Integer nickName) {
		this.nickName = nickName;
	}
	public Integer getMobilePhone() {
		return mobilePhone;
	}
	public void setMobilePhone(Integer mobilePhone) {
		this.mobilePhone = mobilePhone;
	}
	public Integer getPassword() {
		return password;
	}
	public void setPassword(Integer password) {
		this.password = password;
	}
	public Integer getWechatNum() {
		return wechatNum;
	}
	public void setWechatNum(Integer wechatNum) {
		this.wechatNum = wechatNum;
	}
	public Integer getRealName() {
		return realName;
	}
	public void setRealName(Integer realName) {
		this.realName = realName;
	}
	public Integer getIdCardNo() {
		return idCardNo;
	}
	public void setIdCardNo(Integer idCardNo) {
		this.idCardNo = idCardNo;
	}
	public Integer getExtractAmount() {
		return extractAmount;
	}
	public void setExtractAmount(Integer extractAmount) {
		this.extractAmount = extractAmount;
	}
	public Integer getRemainderAmount() {
		return remainderAmount;
	}
	public void setRemainderAmount(Integer remainderAmount) {
		this.remainderAmount = remainderAmount;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	
	
}
